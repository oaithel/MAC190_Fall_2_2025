package com.mac190.introclasses;

public class Sum {
    int x, y;

    public void add(){
        System.out.println(x + " + " + y + " = " + (x+y));
    }
    public void subtract(){
        System.out.println(x + " - " + y + " = " + (x-y));
    }
    public void multiply(){
        System.out.println(x + " * " + y + " = " + (x*y));
    }
    public void divide(){
        System.out.println(x + " / " + y + " = " + (x/y));
    }

}